const sortedPhraseEntries: Partial<Record<Locale, Array<[string, string]>>> = {};

function getSortedPhraseEntries(locale: Locale) {
  sortedPhraseEntries[locale] ??= Object.entries(COMMON[locale] ?? {})
    .sort((a, b) => b[0].length - a[0].length);

  return sortedPhraseEntries[locale] ?? [];
}

function preserveCase(source: string, replacement: string) {
  if (source === source.toUpperCase() && /[A-Z]/.test(source)) return replacement.toUpperCase();
  return replacement;
}

export function translateRuntimeText(value: string, locale: Locale) {
  if (locale === 'en' || !value.trim()) return value;

  const leading = value.match(/^\s*/)?.[0] ?? '';
  const trailing = value.match(/\s*$/)?.[0] ?? '';
  let body = value.trim();
  const phrases = COMMON[locale] ?? {};

  const exact = Object.entries(phrases).find(([source]) => source.toLowerCase() === body.toLowerCase());
  if (exact) return `${leading}${preserveCase(body, exact[1])}${trailing}`;

  for (const [source, target] of getSortedPhraseEntries(locale)) {
    const escaped = source.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const hasWordEdges = /^\w/.test(source) && /\w$/.test(source);
    body = body.replace(new RegExp(hasWordEdges ? `\\b${escaped}\\b` : escaped, 'gi'), (match) => preserveCase(match, target));
  }

  const words = GENERIC_WORDS[locale] ?? {};
  body = body.replace(/\b[A-Za-z][A-Za-z'-]*\b/g, (word) => {
    const lower = word.toLowerCase();
    const replacement = words[lower];
    return replacement ? preserveCase(word, replacement) : word;
  });

  return `${leading}${body}${trailing}`;
}
